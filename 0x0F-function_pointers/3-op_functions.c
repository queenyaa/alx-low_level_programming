#include "3-calc.h"

/**
 * op_add - performs addition
 * @a: 1st number
 * @b: 2nd num
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - performs subtraction
 * @a: 1st number
 * @b: 2nd num
 * Return: subtrac
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - performs multiplication
 * @a: 1st number
 * @b: 2nd num
 * Return: multiply
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - performs division
 * @a: 1st number
 * @b: 2nd num
 * Return: division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - performs modulo
 * @a: 1st number
 * @b: 2nd num
 * Return: modulus
 */

int op_mod(int a, int b)
{
	return (a % b);
}

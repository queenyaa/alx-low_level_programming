/**
 * add - addition
 * @a: integer
 * @b: integer
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtraction
 * @a: int
 * @b: int
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - division
 * @a: int
 * @b: int
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - multiplication
 * @a: int
 * @b: int
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - modulus for remainder
 * @a: int
 * @b: int
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
